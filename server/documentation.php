<p>About cars and pirates! Argh!</p>
<h2>Allgemeines</h2>
<p>Dies soll eine kleine Dokumentation zur Funktion des Programms sein.
Solltest Du fragen haben, dann schreib uns an! Hier geht es zum <a
	href="contact.php">Kontakt</a></p>
<p>CarPirate benötigt keine Installation oder zusätzliche Konfiguration.
Es kann sein, dass Du kein Java hast. Aber mach dir keine Sorgen, denn
das Programm ist in der Lage es festzustellen und Dir entsprechend zu
helfen</p>
<p>Das Programm hat drei Tabs: <i>Suche</i>, <i>Favoriten</i> und <i>Filter</i>.
In jedem Tab wird die entsprechende Information verwaltet. Die Tabs
werden mit Hilfe des Reiters umgeschaltet.</p>
<h2>Suche</h2>
<p>In dem Tab <i>Suche</i> werden aktuelle Suchtreffer in Echtzeit
angezeigt und verwaltet.</p>
<a href="/pix/docs/suche-main.jpg" rel="lightbox"
	title="Suchtab: Hier werden die Ergebnisse angezeigt."><img
	src="/pix/docs/th-suche-main.jpg" alt="Tab Suche" /><br />
<small>Klicken zum Vergr&ouml;&szlig;ern</small></a><br />
<p>Die Tabelle zeigt relevanten Informationen zu Treffern an. Soll ein
Suchtreffer genauer angesehen werden, musst du den Mauszeiger über den
Treffer halten, es werden ggf. das Foto, sowie die Angaben zu der
Ausstattung des Autos und die Adresse des Händlers angezeigt. Beim Doppelklick auf
ein Suchergebnis erfolgt eine Weiterleitung zur detalierten Beschreibung auf der
Autobörse.</p>
<p>Die Tabellenspalten können sowohl in der Breite, als auch in der
Anordnung angepasst werden. Die Präferenzen werden gespeichert und beim
nächsten Start wiederhergestellt.<br />
Durch einfachen Klick auf die Spaltenüberschrift wird die Sortierung
nach den Werten dieser Spalte eingeschaltet. Durch erneuten Klick wird
die Sortierung umgekehrt bzw. ausgeschaltet.</p>
<p>Um den Flut an Suchtreffern zu bewältigen, gibt es in unterem Bereich
des Tabs einige Schaltflächen.</p>
<ul>
	<li><b>Einfrieren</b> Die Anzeige neuen Ergebnissen wird eingefroren.
	Nur die Anzeige, die Suche geht im Hintergrund fleisig weiter. Ein
	weiterer Klick gibt die gesammelte Suchtreffer frei.</li>
	<li><b>Merken</b> Die ausgewählte Autos werden zu Favoriten
	&quot;geparkt&quot;</li>
	<li><b>Entfernen</b> Die ausgewählten Einträge werde aus der Tabelle
	entfernt.</li>
	<li><b>Alle entfernen</b> Alle Einträge werden entfernt.</li>
</ul>
<p>Diese einfache Optionen erlauben die Menge an gefundenen Wagen zu
bearbeiten. Die Zeilen der Tabelle werden, wie üblich mit einfachem
Mausklick ausgewählt, die Tasten <tt>&lt;Shift&gt;</tt> und <tt>&lt;Strg&gt;</tt>
erlauben die Markierung mehrere Einträge gleichzeitig.</p>
<h2>Favorite</h2>
<p>In dem Tab mit <i>Favoriten</i> werden bevorzugte Autos gesammelt.
Die Information zu diesen Autos wird auf der Festplatte gespeichert und
auch wenn die Anzeigen auf den Autobörsen weg sind, bleiben Informationen (bis auf Fotos)
erhalten.</p>
<a href="/pix/docs/favorite-main.jpg" rel="lightbox"
	title="Favoriten: Sammle sie alle!"><img
	src="/pix/docs/th-favorite-main.jpg" alt="Tab Favorite" /><br />
<small>Klicken zum Vergr&ouml;&szlig;ern</small></a><br />
<p>Die Favoriten, die nicht mehr benötigt werden, können mit
entsprechendem Knopf gelöscht werden.</p>
<h2>Tab Filter</h2>
<p>Unter einem Filter wird eine Suchanfrage verstanden, der Strom an
Anzeigen wird nach gegebenen Kriterien gefiltert.</p>
<a href="/pix/docs/filter-main.jpg" rel="lightbox" title="Tab Filter"><img
	src="/pix/docs/th-filter-main.jpg" alt="Tab Filter" /><br />
<small>Klicken zum Vergr&ouml;&szlig;ern</small></a><br />
<p>Hier werden Anfragen zusammengefasst, editiert und gelöscht. Beim
Klicken auf die Schaltfläche <b>Hinzufügen</b> bzw. dem Doppelklick auf
einem Filter in der Tabelle, wird ein neues Fenster aufgemacht, in dem
Eigenschaften eines Autos eingegeben werden können.</p>
<a href="/pix/docs/fform.jpg" rel="lightbox"
	title="Filter: die Beschreibung zu suchenden Auto"><img
	src="/pix/docs/th-fform.jpg" alt="Filterform" /><br />
<small>Klicken zum Vergr&ouml;&szlig;ern</small></a><br />
<h2>Sonstiges</h2>
<p>Beim Klicken auf das Kreuz zum Schließen des Programms, wir es nicht
geschlossen, sondern läuft weiter im Hintergrund. Das kann man auch
daran sehen, dass in dem Systemtray (dort wo die Uhr ist) ein kleines
Wecker-Symbol angezeigt wird. Springt der Symbol von blau <img
	src="/pix/docs/clock.png" /> zu rot <img src="/pix/docs/clock_red.png" />,
so sind neue Suchergebnisse verfügbar.<br />

<br />
Beim Klicken auf dem Traysymbol wir das Programm wieder sichtbar
gemacht. Um CarPirate wirklich zu beenden, wähle in Menü, unter <b>Files&nbsp;&gt;&nbsp;Beenden</b>.
</p>

<p>In dem Menü kann man auch das Aussehen und die Sprache des Programms
ändern, nach dieser Änderungen wird ein Neuestart benötigt.</p>
